SNAPSHOT_FILE = Path(__file__).parent / "data" / "snapshot.json"
TICKER = "COKE"


def _snapshot_mtime() -> float:
    try:
        return SNAPSHOT_FILE.stat().st_mtime
    except FileNotFoundError:
        return 0.0


def load_snapshot() -> dict:
    return _load_snapshot_cached(_snapshot_mtime())


@st.cache_data(show_spinner=False)
def _load_snapshot_cached(mtime: float) -> dict:
    """mtime is included in the cache key (no underscore prefix) so any change to
    snapshot.json on disk auto-busts the cache on the next call."""
    if not SNAPSHOT_FILE.exists():
        raise FileNotFoundError(
            f"{SNAPSHOT_FILE} not found. Run regenerate_scenarios.py to capture the model."
        )
    return json.loads(SNAPSHOT_FILE.read_text())


def snapshot_captured_at() -> datetime | None:
    snap = load_snapshot()
    ts = snap.get("_captured_at")
    if not ts:
        return None
    try:
        return datetime.fromisoformat(ts)
    except ValueError:
        return None


def load_summary(scenario: str = "Base") -> dict:
    """Return summary dict for the chosen scenario, formatted to match the prior schema."""
    snap = load_snapshot()
    static = snap["static"]
    scen = snap["scenarios"][scenario]
    return {
        "years": static["years"],
        "revenue": scen["revenue"],
        "revenue_yoy": scen["revenue_yoy"],
        "ebitda": scen["ebitda"],
        "ebitda_margin": scen["ebitda_margin"],
        "ebitda_yoy": scen["ebitda_yoy"],
        "eps": scen["eps"],
        "eps_yoy": scen["eps_yoy"],
        "valuation": {"years": static["years"], **scen["valuation"]},
        "cap_table_static": static["cap_table_static"],
        "return_eps": scen["return_eps"],
        "return_ebitda": scen["return_ebitda"],
    }


