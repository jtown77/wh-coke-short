@st.cache_data(ttl=300, show_spinner=False)
def load_live_price() -> float:
    t = yf.Ticker(TICKER)
    info = t.info
    p = info.get("regularMarketPrice") or info.get("currentPrice")
    if p is None:
        hist = t.history(period="1d")
        p = float(hist["Close"].iloc[-1])
    return float(p)


@st.cache_data(ttl=900, show_spinner=False)
def load_stock_history(period: str = "max") -> dict:
    t = yf.Ticker(TICKER)
    hist = t.history(period=period, interval="1d")
    if hist.empty:
        return {"dates": [], "close": []}
    return {
        "dates": [d.to_pydatetime() for d in hist.index],
        "close": [float(c) for c in hist["Close"]],
    }


@st.cache_data(ttl=300, show_spinner=False)
def load_intraday(period: str, interval: str) -> dict:
    t = yf.Ticker(TICKER)
    hist = t.history(period=period, interval=interval)
    if hist.empty:
        return {"dates": [], "close": []}
    return {
        "dates": [d.to_pydatetime() for d in hist.index],
        "close": [float(c) for c in hist["Close"]],
    }


@st.cache_data(ttl=86400, show_spinner=False)
def load_oil_quarterly() -> dict:
    """Quarterly close of WTI futures from yfinance, 2016-present."""
    t = yf.Ticker("CL=F")
    hist = t.history(start="2016-01-01", interval="3mo")
    if hist.empty:
        return {"quarters": [], "close": []}
    quarters = []
    for d in hist.index:
        q = (d.month - 1) // 3 + 1
        quarters.append(f"Q{q} {str(d.year)[-2:]}")
    return {"quarters": quarters, "close": [float(c) for c in hist["Close"]]}


def load_commodities_daily(years: int = 3) -> dict:
    """Daily close for CME Midwest US Aluminum (ALI=F, $/MT) + WTI (CL=F, $/bbl)
    over the trailing N years. Aluminum is quoted directly in USD/metric ton on CME.
    """
    from datetime import datetime, timedelta
    start = (datetime.utcnow() - timedelta(days=int(years * 365.25) + 30)).strftime("%Y-%m-%d")

    al = yf.Ticker("ALI=F").history(start=start, interval="1d")
    wti = yf.Ticker("CL=F").history(start=start, interval="1d")

    al_dates = [d.strftime("%Y-%m-%d") for d in al.index] if not al.empty else []
    al_close_mt = [float(c) for c in al["Close"]] if not al.empty else []

    wti_dates = [d.strftime("%Y-%m-%d") for d in wti.index] if not wti.empty else []
    wti_close = [float(c) for c in wti["Close"]] if not wti.empty else []

    return {
        "aluminum": {"dates": al_dates, "close_per_mt": al_close_mt},
        "wti": {"dates": wti_dates, "close_per_bbl": wti_close},
    }


def derive_cap_table(static: dict, live_price: float) -> dict:
    market_cap = live_price * static["diluted_shares"]
    net_debt = static["total_debt"] - static["cash"]
    enterprise_value = market_cap + net_debt + static["nci"]
    return {
        "price": live_price,
        "diluted_shares": static["diluted_shares"],
        "market_cap": market_cap,
        "total_debt": static["total_debt"],
        "cash": static["cash"],
        "net_debt": net_debt,
        "nci": static["nci"],
        "enterprise_value": enterprise_value,
    }
